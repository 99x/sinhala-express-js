function දවටනය(ඔරිජිනල්බඩුව, සිතියම){

    const ප්‍රොක්සිදත්ත = {
        get: (බඩුව, ඈට්‍රිබියුට්නම) => {
            const සිතියම්ගොනුව = `./mappings/${සිතියම}.json`;
            const සිතියම්දත්ත = require(සිතියම්ගොනුව);

            if (සිතියම්දත්ත[ඈට්‍රිබියුට්නම]){
                ඈට්‍රිබියුට්නම = සිතියම්දත්ත[ඈට්‍රිබියුට්නම];
            }

            return බඩුව[ඈට්‍රිබියුට්නම];
        }
    };

    return new Proxy(ඔරිජිනල්බඩුව, ප්‍රොක්සිදත්ත);
};

module.exports =  දවටනය;